//program to find sum of two numbers
#include<stdio.h>
int main()
{
    int a,b,sum;
    //taking input from user
    printf("Enter no 1: ");
    scanf("%d",&a);
    printf("Enter no 2: ");
    scanf("%d",&b);
    //calculating sum
    sum=a+b;
    //printing sum
    printf("Sum = %d",sum);
    return 0;
}

